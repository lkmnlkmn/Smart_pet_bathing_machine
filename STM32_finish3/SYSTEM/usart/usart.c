void Usart_SendString(void){
	u16 t;  
	u16 len;	
	if(USART_RX_STA & 0x8000)
		{
			// 获取接收到的数据长度
			len = USART_RX_STA & 0x3FFF;
			printf("\r\n您发送的消息为:\r\n\r\n");
			for(t=0;t<len;t++)
			{
				USART_SendData(USART1, USART_RX_BUF[t]);//向串口1发送数据
				while(USART_GetFlagStatus(USART1,USART_FLAG_TC)!=SET);//等待发送结束
			}
			printf("\r\n\r\n");//插入换行
			USART_RX_STA=0;
		}
}




